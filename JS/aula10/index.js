// TRABALHANDO COM MANIPULAÇÃO DE STRINGS

let nome = "Pedro Henrique";

console.log(nome.length) // retorna o tamanho do nome
console.log(nome[1]) // RETORNA A SEGUNDA LETRA DO NOME
console.log(nome.slice(-3)) // RETORNA AS 3 ULIMAS LETRAS DO NOME
console.log(nome.split(' ')) // RETORNA OS NOMES SEPARADOS QND ESPAÇO
console.log(nome.toUpperCase()) // RETORNA O NOME EM MAIUSCULO 
console.log(nome.toLowerCase()) // RETORNA O NOME EM MINISCULO